public class Person {
    String name;
    String detail;
    int image;

    public String getName()
    {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image)
    {
        this.image = image;
    }

    public String getDetail()
    {

        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
